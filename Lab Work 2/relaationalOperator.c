#include<stdio.h>
int main(){
    int a = 10, b = 5;
    printf("%d\t>\t%d\t:\t%d\n",a, b,  a > b);
    printf("%d\t<\t%d\t:\t%d\n",a, b,  a < b);
    printf("%d\t==\t%d\t:\t%d\n",a, b,  a == b);
    printf("%d\t<=\t%d\t:\t%d\n",a, b,  a <= b);
    printf("%d\t>=\t%d\t:\t%d\n",a, b,  a >= b);
    printf("%d\t!=\t%d\t:\t%d\n",a, b,  a != b);
    return 0;
}
