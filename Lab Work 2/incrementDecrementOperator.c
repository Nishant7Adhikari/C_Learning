#include<stdio.h>
int main()
{
    int val = 10;
    printf("val = %d\n",val);
    printf("++val = %d\n",++val);
    printf("val++ = %d\n",val++);
    printf("val-- = %d\n",val--);
    printf("--val = %d\n",--val);
    printf("sizeof(int) = %d\n",sizeof(int));
    printf("sizeof(double) = %d\n",sizeof(double));
    printf("sizeof(char) = %d\n");
    return 0;
}
